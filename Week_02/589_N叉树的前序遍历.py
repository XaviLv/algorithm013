# @lc code=start
"""
# Definition for a Node.
class Node:
    def __init__(self, val=None, children=None):
        self.val = val
        self.children = children
"""

class Solution:
    def preorder(self, root: 'Node') -> List[int]:
        if not root: return []
        ans = []
        ans.append(root.val)
        for m in root.children:
            ans.extend(self.preorder(m))
        return ans
# @lc code=end

